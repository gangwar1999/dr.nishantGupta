                                <h3 class="blog-title ls-n-20">What is Angioplasty?</h3>
                               
                                <p>
                                Angioplasty is a method that makes an area in the blocked artery that has plaque constructed inside it. The method of removal of that plaque is referred to as Balloon Angioplasty which makes an area among arteries or widens the artery to permit blood to flow without any problems through the heart.
                                </p>
                                <h3>What does angioplasty treat?</h3>
                                <p>Collection of plaque from the arteries that are made from fats and cholesterol (atherosclerosis) is performed in angioplasty. In Gangasheel Hospital more than 6k angioplasty has been done from all around Uttar Pradesh and Uttarkhand under the supervision of a Cardiac Specialist.</p>
                                <h3 class="blog-subtitle ls-n-20">Some diseases of arteries are:-</h3>
                                <p>
                               <b>Coronary artery disease: -</b> When arteries with inside get blocked or narrowed by the deposition of plaque makes a hurdle for the flow of oxygenated blood in the heart and ended up in chest pain and coronary heart attack. Coronary angioplasty or percutaneous coronary intervention is suggested by doctors to get rid of all of the plaque from the arteries.
                                </p>
                                <p>
                               <b> When Peripheral artery disease: -</b> In this type of disease, arteries in the arms, legs, and pelvis get blocked by plaque which may cause leg pain while walking and pain in the hand while working. In that case, the doctor recommends angioplasty.
                                </p>
                                <p>
                               <b>Carotid artery diseases:-</b> Blocked arteries in the neck can result in stroke if the mind doesn’t get sufficient oxygen. Angioplasty can assist to get rid of plaque from the arteries and makes way for blood supply.
                                </p>
                                <p>
                            <b>Plaque in the kidneys arteries:-</b> Blocked  When plaque gets collects in the arteries to your kidneys, it influences the oxygen delivered to your kidneys. Renal Artery Angioplasty can assist with this kind of artery disease.
                                </p>
                                <h3>Stenting</h3>
                                <p>
                                    “The process of putting a stent (small metal wire tube) in a blocked artery is called stenting. Stenting is a minimally invasive procedure, which is not major surgery. Stenting is used to widen the blocked artery in the heart. Some stents were coated with a drug that prevents arteries from narrowing again. Stent can be made up of metal mesh, silicon, fabric, or an amalgamation of materials.”
                                </p>
