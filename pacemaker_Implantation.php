                                <h3 class="blog-title ls-n-20">What is Pacemaker?</h3>
                               
                                <p>
                                Pacemaker is a small battery operated device which is placed (implanted) in the chest to help control the heartbeat. This device is also called cardiac pacing device. It is used to prevent the heart from beating too slow. Implanting a pacemaker in the chest requires a surgical procedure.
                                </p>
                                <h3>Types of Pacemaker</h3>
                                <p>There are 3 types of pacemaker, depending upon your condition, you might have one of the following types:-</p>
                            <ul class="mb-4">
                                <li><a><i class="fas fa-caret-right"></i>Single chamber pacemaker:-</a>This type usually carries electrical impulses to the right ventricle of your heart.
                                    </li>
                                <li><a><i class="fas fa-caret-right"></i>Dual chamber pacemaker:-</a>This type carries electrical impulses to the right ventricle and the right atrium of your heart to help control the timing of contractions between the two chambers.
                                </li>
                                <li><a><i class="fas fa-caret-right"></i>Biventricular pacemaker:- </a>Biventricular pacing, also called cardiac resynchronization therapy is for people who have heart failure and heartbeat problems. This type of pacemaker stimulates both of the lower heart chambers (the right and left ventricles) to make the heart beat more efficiently.</li>
                            </ul>

                            
                                <h3 class="blog-subtitle ls-n-20">How Pacemaker device work?</h3>

                                <ul>
                                    <li><i class="fas fa-caret-right"></i>It is a small battery operated device about the size of a matchbox or smaller that weighs 20 to 50g.</li>
                                    <li><i class="fas fa-caret-right"></i>It consists of a pulse generator, which has a battery and a tiny computer circuit, and 1 or more wires known as pacing leads, which attach to your heart.</li>
                                    <li><i class="fas fa-caret-right"></i>The pulse generator emits electrical impulses through the wires to your heart. The rate at which the electrical impulses are sent out is called the pacing rate.</li>
                                    <li><i class="fas fa-caret-right"></i>Almost all modern pacemakers work on demand. This means they can be programmed to adjust the discharge rate in response to your body's needs.</li>
                                    <li><i class="fas fa-caret-right"></i>If the pacemaker senses that your heart has missed a beat or is beating too slow, it sends signals at a steady rate.</li>
                                    <li><i class="fas fa-caret-right"></i>If it senses that your heart is beating normally by itself, it does not send out any signals.</li>
                                    <li><i class="fas fa-caret-right"></i>Most pacemakers have a special sensor that recognises body movement or your breathing rate.</li>
                                    <li><i class="fas fa-caret-right"></i>This allows it to speed up the discharge rate when you're active.</li>
                                </ul>
